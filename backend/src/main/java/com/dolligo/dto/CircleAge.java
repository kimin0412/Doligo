package com.dolligo.dto;

public class CircleAge {
	int teen;
	int second;
	int third;
	int forth;
	int above;
	public int getTeen() {
		return teen;
	}
	public void setTeen(int teen) {
		this.teen = teen;
	}
	public int getSecond() {
		return second;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	public int getThird() {
		return third;
	}
	public void setThird(int third) {
		this.third = third;
	}
	public int getForth() {
		return forth;
	}
	public void setForth(int forth) {
		this.forth = forth;
	}
	public int getAbove() {
		return above;
	}
	public void setAbove(int above) {
		this.above = above;
	}
	@Override
	public String toString() {
		return "CircleAge [teen=" + teen + ", second=" + second + ", third=" + third + ", forth=" + forth + ", above="
				+ above + "]";
	}
	
}
