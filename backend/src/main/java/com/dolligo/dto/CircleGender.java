package com.dolligo.dto;

public class CircleGender {
	int a;
	int w;
	int m;
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getM() {
		return m;
	}
	public void setM(int m) {
		this.m = m;
	}
	@Override
	public String toString() {
		return "CircleGender [a=" + a + ", w=" + w + ", m=" + m + "]";
	}
	
}
